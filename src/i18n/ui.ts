export type UIKey = keyof (typeof ui)['es'];

/** Devuelve el idioma a partir de la URL (/es/..., /en/..., /gl/..., /eu/..., /ca/...). */
export function getLangFromUrl(url: URL): Lang {
  const [, lang] = url.pathname.split('/');
  if (lang in ui) return lang as Lang;
  return defaultLang;
}

/** Normaliza un parámetro de ruta a un idioma soportado (cae al idioma por defecto). */
export function normalizeLang(value: string | undefined): Lang {
  return value && value in ui ? (value as Lang) : defaultLang;
}

/**
 * Idioma del CONTENIDO editorial (datos JSON, textos legales, TMDB), que solo
 * existe en español e inglés. La interfaz en gallego, euskera o catalán muestra
 * el contenido en español (la lengua original del proyecto).
 */
export type ContentLang = 'es' | 'en';
export function contentLang(lang: Lang): ContentLang {
  return lang === 'en' ? 'en' : 'es';
}

/** Locale BCP-47 de cada idioma para Intl (fechas y números). */
export const locales: Record<Lang, string> = {
  es: 'es-ES',
  en: 'en-GB',
  gl: 'gl-ES',
  eu: 'eu-ES',
  ca: 'ca-ES',
};

/** Crea una función traductora `t('clave')` para el idioma dado. */
export function useTranslations(lang: Lang) {
  return function t(key: UIKey): string {
    return ui[lang][key] ?? ui[defaultLang][key];
  };
}

/**
 * Devuelve el campo localizado `<base>_<lang>` de un objeto de datos propio
 * (JSON del repo), con reserva al español si ese idioma aún no está traducido.
 * Permite ir traduciendo el contenido a gl/eu/ca sin romper nada por el camino.
 * p.ej. localizedField(item, 'blurb', 'gl') -> item.blurb_gl ?? item.blurb_es
 */
export function localizedField<T>(obj: T, base: string, lang: Lang): string {
  const rec = obj as Record<string, unknown>;
  const v = rec[`${base}_${lang}`] ?? rec[`${base}_es`];
  return typeof v === 'string' ? v : '';
}

/** Construye una ruta con prefijo de idioma. p.ej. localizePath('es', 'archive') -> '/es/archive' */
export function localizePath(lang: Lang, path = ''): string {
  const clean = path.replace(/^\/+|\/+$/g, '');
  return clean ? `/${lang}/${clean}` : `/${lang}`;
}

/** Formatea una fecha según el idioma. */
export function formatDate(date: Date, lang: Lang): string {
  return new Intl.DateTimeFormat(locales[lang], {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
  }).format(date);
}
